export type Lang = keyof typeof ui;
type Key = keyof (typeof ui)["en"];

export function useTranslations(lang: Lang) {
  return function t(key: Key): string {
    return ui[lang][key] ?? ui.en[key];
  };
}

// {n} だけを差し込む軽量補間。文言(テンプレ)は ui.ts が唯一の源(SSOT)、
// ここは数値を埋めるだけ。捏造値を渡さないのは呼び出し側(data 由来のみ)の責務。
export function tn(lang: Lang, key: Key, n: number | string): string {
  const tpl = ui[lang][key] ?? ui.en[key];
  return tpl.replace("{n}", String(n));
}
